
public class MyCalculator1 {
	public int addnums(int num1, int num2) {
		int result = num1 + num2;
		return result;
	}
	
	public int subnums(int num1, int num2) {
		int result = num1 - num2;
		return result;
	}
	
	public int mulnums(int num1, int num2) {
		int result = num1 * num2;
		return result;
	}
	
	public int divnums(int num1, int num2) {
		int result = num1 / num2;
		return result;
	}

}
